        private Texture _diffuseMap;
        private Texture _specularMap;
        private Vector3 _position;
        public Vector3 Position => _position;
        private Shader _shader;
        private float rotX;
        private float rotY;
        private float rotZ;
        private Matrix4 _rotation;
        private int _index = 0;
        private static int _counter = 0;
        private static Random Random = new Random();

        public static int Use()
        {
            var buffer = GL.GenBuffer();
            GL.BindBuffer(BufferTarget.ArrayBuffer, buffer);
            GL.BufferData(BufferTarget.ArrayBuffer, VertexData.Length * sizeof(float), VertexData, BufferUsageHint.StaticDraw);

            return buffer;
        }

        public Cube(string diffuse, string specular, Vector3 position, Shader shader)
        {
            rotX = Random.Next(0, 90);
            rotY = Random.Next(0, 90);
            rotZ = Random.Next(0, 90);
            //rotX = 0;
            //rotY = 0;
            //rotZ = 0;

            _diffuseMap = Texture.LoadFromFile(diffuse);
            _specularMap = Texture.LoadFromFile(specular);
            _rotation = Matrix4.CreateRotationX(rotX) * Matrix4.CreateRotationY(rotY) * Matrix4.CreateRotationZ(rotZ);
            _position = position;
            _shader = shader;
            _index = _counter;
            _counter++;
        }

        public bool Collision(Vector3 point)
        {
            var box = _position;
            return (
                (point.X >= box.X - 0.51f && point.X <= box.X + 0.51f) &&
                (point.Y >= box.Y - 0.51f && point.Y <= box.Y + 0.51f) &&
                (point.Z >= box.Z - 0.51f && point.Z <= box.Z + 0.51f));
        }

        public void Draw()
        {
            var model = _rotation * Matrix4.CreateTranslation(_position);

            _diffuseMap.Use(TextureUnit.Texture0);
            _specularMap.Use(TextureUnit.Texture1);

            _shader.SetInt("material.diffuse", 0);
            _shader.SetInt("material.specular", 1);
            _shader.SetVector3("material.specular", new Vector3(0.5f, 0.5f, 0.5f));
            _shader.SetFloat("material.shininess", 32.0f);
            _shader.SetMatrix4("model", model);

            GL.DrawArrays(PrimitiveType.Triangles, 0, 36);
        }
